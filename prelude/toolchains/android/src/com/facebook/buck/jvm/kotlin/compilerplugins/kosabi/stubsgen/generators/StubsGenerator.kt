package com.facebook.kotlin.compilerplugins.kosabi.stubsgen.generators

interface StubsGenerator {
  /**
   * Called multiple times in order to create new code. Note that instances should not rely on being
   * able to resolve [projectFiles] to descriptors. They should rather use the Psi APIs to parse
   * files.
   *
   * In the first round code generators are called with the files from the project. In following
   * rounds [projectFiles] represents files that were generated by code generators until no code
   * generator produces any files anymore.
   */
  fun generateStubs(context: GenerationContext)
}
